
public class Ejercicio_08 {

    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        int resultado = a / b;

        System.out.println("Resultado: " + resultado);

        /*
        PRUEBA DE ESCRITORIO:

        a = 5
        b = 2

        Se realiza la división: 5 / 2 = 2.5

        Pero como ambas variables son int:
        Java elimina los decimales

        Entonces:
        resultado = 2

        Por eso el resultado final es 2.
        */
    }

}
